package javapackage;

public class ArraysEx {
	
	public static void main(String[] args) {
		
		//array syntax
		int a[] = {10, 20, 30, 40, 50};
		
	    int count = a.length;
	    System.out.println("array values count: " + count);
	    
		for (int i = 0; i < count; i++) {
			System.out.println(a[i]);
		}
		
		
	}

}
