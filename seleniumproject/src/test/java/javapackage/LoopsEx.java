package javapackage;

public class LoopsEx {

	public static void main(String[] args) {

		// Loops in java 1-10

		//excel
		
		// for
		int i;
		for (i = 1; i <= 10; i++) {
			System.out.println(i);
		}

		i++; // i = i+1;

		// while

		int a = 10;

		while (a >= 1) {
			System.out.println(a);
			a--;           // a = a-1;
		}

	}

}
